def _ability(
    ability_id: str,
    name: str,
    *,
    damage: str | None = None,
    damage_type: str = 'slashing',
    attack_bonus: int | None = None,
    description: str | None = None,
    ability_type: str = 'attack',
    range_band: str = 'melee',
    target_type: str = 'single',
    cooldown: str = 'none',
    conditions: list[str] | None = None,
    save: dict[str, Any] | None = None,
    use_when: list[str] | None = None,
) -> dict[str, Any]:
    payload: dict[str, Any] = {
        'id': ability_id,
        'name': name,
        'type': ability_type,
        'description': description or name,
        'actionCost': 'action',
        'range': range_band,
        'targetType': target_type,
        'cooldown': cooldown,
        'aiUseWhen': use_when or [],
    }
    if damage:
        payload['damage'] = {'dice': damage, 'type': damage_type}
    if attack_bonus is not None:
        payload['attackBonus'] = attack_bonus
    if conditions:
        payload['conditionsApplied'] = conditions
    if save:
        payload['save'] = save
    return payload


def _creature(
    creature_id: str,
    name: str,
    *,
    creature_type: str,
    level: int,
    tier: str,
    size: str = 'medium',
    hp: int,
    ac: int,
    scores: tuple[int, int, int, int, int, int],
    movement: dict[str, int] | None = None,
    senses: dict[str, int] | None = None,
    tags: list[str] | None = None,
    abilities: list[dict[str, Any]] | None = None,
    behavior: dict[str, Any] | None = None,
    resistances: list[str] | None = None,
    vulnerabilities: list[str] | None = None,
    immunities: list[str] | None = None,
    xp: int = 0,
    hints: list[str] | None = None,
) -> dict[str, Any]:
    strength, dexterity, constitution, intelligence, wisdom, charisma = scores
    creature = normalize_creature_definition(
        {
            'id': creature_id,
            'version': 1,
            'name': name,
            'source': 'core_bestiary',
            'descriptionShort': f'A reliable core bestiary {name.lower()}.',
            'descriptionLong': f'{name} is a reusable encounter creature with deterministic combat behavior and bounded mechanics.',
            'creatureType': creature_type,
            'visualTags': tags or [creature_type],
            'level': level,
            'challengeTier': tier,
            'size': size,
            'stats': {
                'maxHp': hp,
                'armorClass': ac,
                'strength': strength,
                'dexterity': dexterity,
                'constitution': constitution,
                'intelligence': intelligence,
                'wisdom': wisdom,
                'charisma': charisma,
            },
            'movement': movement or {'walk': 30},
            'senses': senses or {'passivePerception': 10},
            'resistances': resistances or [],
            'vulnerabilities': vulnerabilities or [],
            'immunities': immunities or [],
            'abilities': abilities or [],
            'behavior': behavior or {},
            'xpReward': xp,
            'aiNarrationHints': hints or [],
        },
        source='core_bestiary',
    )
    creature['balance'] = analyze_creature_balance(
        creature,
        party_level=max(1, level),
        party_size=4,
        target_difficulty=tier,
    )
    creature['balance']['reviewed'] = True
    return creature


