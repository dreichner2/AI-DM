CORE_BESTIARY_BY_ID = {creature['id']: creature for creature in CORE_BESTIARY}


def core_bestiary() -> list[dict[str, Any]]:
    return [deepcopy(creature) for creature in CORE_BESTIARY]


def core_creature(creature_id: str) -> dict[str, Any] | None:
    creature = CORE_BESTIARY_BY_ID.get(creature_id)
    return deepcopy(creature) if creature else None
